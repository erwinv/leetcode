function threeSum(nums: number[]): number[][] {
  nums.sort((a, b) => a - b);

  const ret: number[][] = [];

  for (const [i, x] of nums.entries()) {
    if (i > 0 && x === nums[i - 1]) continue;

    for (const [y, z] of iterateBothSides(
      nums,
      i + 1,
      nums.length,
      (y, z) => x + y + z
    )) {
      if (x + y + z === 0) ret.push([x, y, z]);
    }
  }

  return ret;
}

function* iterateBothSides<T>(
  xs: T[],
  from = 0,
  end = xs.length,
  whichSide: (x: T, y: T) => number
): Iterable<[T, T]> {
  let i = from;
  let j = end - 1;

  while (i < j) {
    const x = xs[i];
    const y = xs[j];

    yield [x, y];

    const side = whichSide(x, y);

    if (side < 0) {
      i++;
    } else if (side > 0) {
      j--;
    } else {
      j--;
      while (xs[j] === xs[j + 1]) j--;
    }
  }
}

